/* RESPOSTA:
 * 
 *      Bubble Sort: Ordena os valores em forma decrescente, a posição atual é comparada com a próxima posição,
 *                   se a posição atual for maior que a posição posterior, é realizada a troca dos valores nessa posição. 
 *                   Caso contrário, não é realizada a troca, apenas passa-se para o próximo par de comparações.
 *      
 *      Selection Sort: Selecionar o menor item e colocar na primeira posição e por assim em diante.
 * 
 *      Insertion Sort: Percorre um vetor de elementos da esquerda para a direita ordenando os elementos à esquerda. 
 * 
 *      Quick Sort: Divide em partes menores, as partes menores são ordenados independentemente e os resultados são combinados para produzir a solução final.
 * 
 *      Merge Sort: Divide em partes menores, resolve e junta as partes. O vetor será dividido em duas partes iguais, 
 *                  que serão cada uma divididas em duas partes, e assim até ficar um ou dois elementos cuja ordenação é trivial.
 */